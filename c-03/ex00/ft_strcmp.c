int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	d;

	i = 0;
	d = 0;
	while ((s1[i]) && (s2[i]) && (d == 0))
	{
		d = s1[i] - s2[i];
		if ((s1[i + 1] == '\0') || (s2[i + 1] == '\0'))
			d = s1[i + 1] - s2[i + 1];
		i++;
	}
	return (d);
}
