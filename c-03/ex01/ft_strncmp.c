int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int	i;
	unsigned int	d;

	i = 0;
	d = 0;
	while ((s1[i]) && (s2[i]) && (d == 0) && (i < n))
	{
		d = s1[i] - s2[i];
		if (((s1[i + 1] == '\0') || (s2[i + 1] == '\0')) && !(i + 1 == n))
			d = s1[i + 1] - s2[i + 1];
		i++;
	}
	return (d);
}
